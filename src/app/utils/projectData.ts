export const editFeatureName = (featureIndex: number, newName: string) => {
  projectData[featureIndex].name = newName;
}

export const deleteFeature = (featureId: number) => {
  const featureIndex = projectData.findIndex(feature => feature.id === featureId);
  projectData.splice(featureIndex, 1);
}

export const addNewFeature = (featureName: string) => {
  projectData.push({
    id: new Date().getTime(),
    name: featureName,
    status: STATUS.ToDo,
    expanded: false,
    tasks: [],
  });
}

export const getProjectData = () => {
  return projectData;
}

export const getTasks = () => {
  return tasks;
}

const isTaskInFeature = (taskId: number, feature: Feature) => !!feature?.tasks.some(task => task.id === taskId)

export const getFeatureOfTask = (taskId: number) => {
  return projectData.find(feature => {
    return isTaskInFeature(taskId, feature);
  });
}
